package com.yyds.unit2.demo;
public class Demo1Variable {
    public static void main(String[] args) {
        int num = 10;
        double num2 = 3.14;
        // long类型数据以L结尾
        long num3 = 100L;
        float num4 = 3.14F;
        char c = 'A';
        System.out.println(num);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(c);
    }
}
